# -----------------------------------------------------------------
# Формула П2.1: скорость V(D)
# -----------------------------------------------------------------

def test_velocity_free():
    """При D <= D0 скорость равна V0."""
    # М0-3, горизонтальный: V0=100, D0=0.51
    V = velocity(0.3, "горизонтальный", "М0-3")
    assert V == pytest.approx(100.0)


def test_velocity_free_exact_d0():
    """При D = D0 скорость равна V0."""
    V = velocity(0.51, "горизонтальный", "М0-3")
    assert V == pytest.approx(100.0)


def test_velocity_dense():
    """При D > D0 скорость снижается по формуле П2.1."""
    # М0-3, горизонтальный: V0=100, a=0.295, D0=0.51
    D = 1.0
    V0, a, D0 = 100.0, 0.295, 0.51
    expected = V0 * (1.0 - a * math.log(D / D0))
    V = velocity(D, "горизонтальный", "М0-3")
    assert V == pytest.approx(expected, rel=1e-6)


def test_velocity_zero_density():
    """При D=0 скорость равна V0."""
    V = velocity(0.0, "горизонтальный", "М0-3")
    assert V == pytest.approx(100.0)


# -----------------------------------------------------------------
# Формула П2.2: интенсивность q = V * D
# -----------------------------------------------------------------

def test_intensity():
    """q = V * D - формула П2.2."""
    D = 0.3
    q = intensity(D, "горизонтальный", "М0-3")
    V = velocity(D, "горизонтальный", "М0-3")
    assert q == pytest.approx(V * D)


def test_intensity_zero():
    """При D=0 интенсивность равна 0."""
    q = intensity(0.0, "горизонтальный", "М0-3")
    assert q == pytest.approx(0.0)


# -----------------------------------------------------------------
# Коэффициент m для проемов
# -----------------------------------------------------------------

def test_door_coefficient_m_low_density():
    """m = 1.0 при D < 0.5 для проемов."""
    # М0-3, проем: V0=100, D0=0.65
    # При D=0.3 < 0.5: m=1.0, и D < D0=0.65 => V=V0=100
    V = velocity(0.3, "проем", "М0-3")
    assert V == pytest.approx(100.0)  # V0 * 1.0


def test_door_coefficient_m_high_density():
    """m = 1.25 - 0.05*D при D >= 0.5 для проемов."""
    D = 1.0
    V0, a, D0 = 100.0, 0.295, 0.65
    V_base = V0 * (1.0 - a * math.log(D / D0))
    m = 1.25 - 0.05 * D  # = 1.20
    expected = V_base * m
    V = velocity(D, "проем", "М0-3")
    assert V == pytest.approx(expected, rel=1e-6)


# -----------------------------------------------------------------
# Dmax - плотность при qmax
# -----------------------------------------------------------------

def test_dmax():
    """Dmax = D0 * exp(1 - 1/a)."""
    _, a, D0 = FLOW_PARAMS_M0["М0-3"]["горизонтальный"]
    expected = D0 * math.exp(1.0 - 1.0 / a)
    assert density_at_qmax("горизонтальный", "М0-3") == pytest.approx(expected, rel=1e-6)


