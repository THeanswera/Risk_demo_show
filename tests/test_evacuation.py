# -----------------------------------------------------------------
# Разные группы дают разные результаты
# -----------------------------------------------------------------

def test_different_groups():
    """Разные группы М0-1...М0-7 дают разные tр."""
    segments = [
        {"тип": "горизонтальный", "длина": 20.0, "ширина": 4.0, "людей": 50},
        {"тип": "проем", "длина": 0.0, "ширина": 1.5, "людей": 0},
        {"тип": "горизонтальный", "длина": 30.0, "ширина": 2.0, "людей": 0},
    ]
    results = {}
    for group in ["М0-1", "М0-3", "М0-7"]:
        r = calc_evacuation(segments, group, 0.10)
        results[group] = r["t_p"]

    # М0-7 (с грудными) медленнее М0-1 (дети) медленнее М0-3 (трудоспособные)?
    # На самом деле порядок зависит от V0; проверяем что все разные
    values = list(results.values())
    assert len(set(round(v, 6) for v in values)) == len(values), \
        f"Результаты должны различаться: {results}"


