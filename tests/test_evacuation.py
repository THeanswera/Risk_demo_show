# -----------------------------------------------------------------
# Маршрут без скоплений
# -----------------------------------------------------------------

def test_simple_route():
    """Маршрут без скоплений: tр = сумма ti."""
    segments = [
        {"тип": "горизонтальный", "длина": 10.0, "ширина": 6.0, "людей": 10},
        {"тип": "проем", "длина": 0.0, "ширина": 2.0, "людей": 0},
        {"тип": "горизонтальный", "длина": 15.0, "ширина": 3.0, "людей": 0},
    ]
    result = calc_evacuation(segments, "М0-3", 0.10)
    assert result["t_p"] > 0
    assert result["t_ck"] == 0.0
    assert len(result["congestion_segments"]) == 0

    # tр = сумма ti
    sum_t = sum(s["t"] for s in result["segments_detail"])
    assert result["t_p"] == pytest.approx(sum_t, rel=1e-3)


