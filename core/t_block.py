# ───────────────────────────────────────────────────────────────
# Критические значения ОФП (п. 25 Методики №1140)
# ───────────────────────────────────────────────────────────────

_T0 = 23.0        # начальная температура, °С
_T_CR = 70.0      # критическая температура, °С
_DT_CR = _T_CR - _T0   # = 47 К

# Критические концентрации газов (кг/м³)
_C_O2_0 = 0.272          # начальная концентрация O₂
_C_O2_CR = 0.226         # критическая концентрация O₂
_C_CO2_CR = 0.11         # CO₂
_C_CO_CR = 1.16e-3       # CO
_C_HCl_CR = 23.0e-6      # HCl

# Видимость: путь просматривается не более l_пр = 20 м
_L_PR = 20.0             # м
# Критическая оптическая плотность (Нп/м): D_кр = 2.38 / l_пр
# (из условия: освещённость на расстоянии l_пр составляет e^(-D_кр·l_пр) от исходной,
#  с порогом 5% → D_кр = -ln(0.05)/l_пр ≈ 2.996/l_пр;
#  по ГОСТ 12.1.004 используется 2.38/l_пр)
# TODO: уточнить значение коэффициента по тексту Приложения 6 Методики №1140
_D_CR = 2.38 / _L_PR     # Нп/м

# Физические константы
_CP = 1.0          # удельная теплоёмкость воздуха, кДж/(кг·К)
_RHO0 = 1.2        # плотность воздуха при T0, кг/м³
_ETA_Q = 0.5       # доля теплоты, передаваемой газовой среде помещения
# TODO: η_q = 0.5 — приближённое значение; уточнить по Приложению 6 Методики №1140


def _t_crit_from_mass(w_cr: float, psi_ud: float, v_l: float) -> float:
    """
    Критическое время (с) через критическую массу продуктов горения W_кр.

    Модель: круговое развитие пожара, свободное горение.
    Масса сгоревшего материала: W(t) = ψ_уд · π · v_л² · t³ / 3
    Из W(t_кр) = W_кр: t_кр = (3·W_кр / (ψ_уд·π·v_л²))^(1/3)

    TODO: уточнить модель развития пожара (круговое/линейное/угловое)
          по Приложению 6 Методики №1140.
    """
    if psi_ud <= 0 or v_l <= 0 or w_cr <= 0:
        return float("inf")
    return (3.0 * w_cr / (psi_ud * math.pi * v_l ** 2)) ** (1.0 / 3.0)


def calc_t_block(
    fire_load_type: str,
    area_m2: float,
    height_m: float,
    eta: float = 0.8,
) -> dict:
    """
    Аналитический расчёт критического времени блокирования tбл (мин)
    по Приложению 6 Методики №1140 (зональная модель, свободное горение).

    Параметры
    ----------
    fire_load_type : вид помещения из Таблицы П9.1
    area_m2        : площадь помещения, м²
    height_m       : высота помещения, м
    eta            : коэффициент свободного объёма (η), по умолчанию 0.8

    Возвращает
    ----------
    dict с ключами:
        t_bl            : tбл (мин) — минимальное критическое время
        t_temp          : критическое время по температуре (мин)
        t_vis           : критическое время по видимости (мин)
        t_o2            : критическое время по O₂ (мин)
        t_co2           : критическое время по CO₂ (мин)
        t_co            : критическое время по CO (мин)
        t_hcl           : критическое время по HCl (мин)
        limiting_factor : определяющий ОФП
        V_sw            : свободный объём помещения, м³

    ПРЕДУПРЕЖДЕНИЕ: Реализация основана на зональной модели свободного горения.
    Для точного определения tбл используйте полевое моделирование (FDS, PyroSim).
    TODO: Сверить формулы с Приложением 6 Методики МЧС № 1140.
    """
    row = FIRE_LOAD_TABLE.loc[FIRE_LOAD_TABLE["Вид помещения"] == fire_load_type]
    if row.empty:
        raise ValueError(f"Вид помещения «{fire_load_type}» не найден в Таблице П9.1")

    r = row.iloc[0]
    q_n = safe_float(r["Q_н (МДж/кг)"])         # МДж/кг
    d_m = safe_float(r["D_m (Нп·м²/кг)"])        # Нп·м²/кг
    psi_ud = safe_float(r["ψ_уд (кг/(м²·с))"])   # кг/(м²·с)
    v_l = safe_float(r["v_л (м/с)"])              # м/с
    l_ud = safe_float(r["L_уд (кг/кг)"])          # кг воздуха / кг материала
    nu_co2 = safe_float(r["CO₂ (кг/кг)"])         # выход CO₂
    nu_co = safe_float(r["CO (кг/кг)"])            # выход CO
    nu_hcl = safe_float(r["HCl (кг/кг)"])         # выход HCl

    # Свободный объём помещения, м³
    v_sw = eta * safe_float(area_m2) * safe_float(height_m)

    # ── Критические массы для каждого ОФП ──────────────────────

    # 1. Температура (ΔT = 47°C)
    # Из теплового баланса: ΔT = η_q · Q_н · W_кр / (c_p · ρ₀ · V_св)
    # W_кр_T = ΔT · c_p · ρ₀ · V_св / (η_q · Q_н · 1000)
    # (Q_н в МДж/кг → ×1000 для кДж/кг, c_p в кДж/(кг·К))
    # TODO: уточнить коэффициент η_q и формулу по тексту Приложения 6 Методики №1140
    w_cr_temp = (_DT_CR * _CP * _RHO0 * v_sw) / (_ETA_Q * q_n * 1000.0) if q_n > 0 else float("inf")

    # 2. Видимость (l_пр = 20 м, D_кр = 2.38/20 Нп/м)
    # α(t) = D_m · W(t) / V_св = D_кр → W_кр_vis = D_кр · V_св / D_m
    # TODO: уточнить формулу расчёта критической оптической плотности по Приложению 6
    w_cr_vis = (_D_CR * v_sw / d_m) if d_m > 0 else float("inf")

    # 3. Кислород O₂
    # Потребление O₂: ΔC_O2 = L_уд · W(t) / V_св
    # Критическое: L_уд · W_кр_O2 / V_св = C_O2_0 - C_O2_кр
    w_cr_o2 = ((_C_O2_0 - _C_O2_CR) * v_sw / l_ud) if l_ud > 0 else float("inf")

    # 4. CO₂
    # C_CO2(t) = ν_CO2 · W(t) / V_св → W_кр_CO2 = C_CO2_кр · V_св / ν_CO2
    w_cr_co2 = (_C_CO2_CR * v_sw / nu_co2) if nu_co2 > 0 else float("inf")

    # 5. CO
    w_cr_co = (_C_CO_CR * v_sw / nu_co) if nu_co > 0 else float("inf")

    # 6. HCl
    w_cr_hcl = (_C_HCl_CR * v_sw / nu_hcl) if nu_hcl > 0 else float("inf")

    # ── Критические времена (с) ──────────────────────────────────
    t_temp_s = _t_crit_from_mass(w_cr_temp, psi_ud, v_l)
    t_vis_s = _t_crit_from_mass(w_cr_vis, psi_ud, v_l)
    t_o2_s = _t_crit_from_mass(w_cr_o2, psi_ud, v_l)
    t_co2_s = _t_crit_from_mass(w_cr_co2, psi_ud, v_l)
    t_co_s = _t_crit_from_mass(w_cr_co, psi_ud, v_l)
    t_hcl_s = _t_crit_from_mass(w_cr_hcl, psi_ud, v_l)

    # Определяем лимитирующий фактор
    factors = {
        "температура (70°С)": t_temp_s,
        "видимость (20 м)": t_vis_s,
        "O₂ (0.226 кг/м³)": t_o2_s,
        "CO₂ (0.11 кг/м³)": t_co2_s,
        "CO (1.16·10⁻³ кг/м³)": t_co_s,
        "HCl (23·10⁻⁶ кг/м³)": t_hcl_s,
    }
    limiting_factor = min(factors, key=lambda k: factors[k])
    t_bl_s = factors[limiting_factor]

    def _s_to_min(t_s: float) -> Optional[float]:
        if math.isinf(t_s):
            return None
        return round(t_s / 60.0, 2)

    return {
        "t_bl": _s_to_min(t_bl_s),
        "t_temp": _s_to_min(t_temp_s),
        "t_vis": _s_to_min(t_vis_s),
        "t_o2": _s_to_min(t_o2_s),
        "t_co2": _s_to_min(t_co2_s),
        "t_co": _s_to_min(t_co_s),
        "t_hcl": _s_to_min(t_hcl_s),
        "limiting_factor": limiting_factor,
        "V_sw": round(v_sw, 2),
    }
